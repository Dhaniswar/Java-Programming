    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.jPasswordField1.getText().equals(loginList.get(this.jComboBox2.getSelectedIndex()).Password))
        {
            this.setVisible(false);
            MainJFrame app=new MainJFrame();
            app.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            app.setTitle("Course Management System!!");
            app.CustomizeUserFeatures(loginList.get(this.jComboBox2.getSelectedIndex()));
            app.setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(new JFrame(),"Login failed. Try again."); 
            this.jPasswordField1.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private void GetLoginDetail()
    {
        this.jComboBox2.removeAllItems();
        loginList=ServiceClass.LoadUserLogin(this.jComboBox1.getSelectedItem().toString());
        for(Login l:loginList)
        {
           this.jComboBox2.addItem(l.UserName+"-"+l.UserID);
        } 
    }
