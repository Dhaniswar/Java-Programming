    private void AddModule(boolean isElective){
        String newModule = JOptionPane.showInputDialog(null, "Enter module name.");
        String coursename=this.jComboBox1.getSelectedItem().toString();
        String levelname=this.jComboBox2.getSelectedItem().toString();
        try (Connection con = DriverManager.getConnection(MainJFrame.connectionUrl);)
        {
            String SQL = "Insert into ModuleList(MName,LevelID,IsElective) values(?,(Select LevelID from LevelList Where LevelName Like '"+levelname+"' And CourseID=(Select CID from courseList where Coursename like '"+coursename+"')),?);";
            PreparedStatement preparedStmt = con.prepareStatement(SQL);
            preparedStmt.setString (1, newModule);
            preparedStmt.setBoolean(2, isElective);
            // execute the preparedstatement
            preparedStmt.execute();
            JOptionPane.showMessageDialog(new JFrame(),"Module added successfully.");
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(new JFrame(),"Error while adding module."+ex);
        }
        if(this.jComboBox1.getSelectedIndex()>=0 && this.jComboBox2.getSelectedIndex()>=0)
        LoadModule(coursename,levelname);
    }
