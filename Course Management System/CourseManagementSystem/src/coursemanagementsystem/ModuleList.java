    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedid = (int)this.jTable1.getModel().getValueAt(this.jTable1.getSelectedRow(), 0);
        try (Connection con = DriverManager.getConnection(MainJFrame.connectionUrl);)
        {
            String SQL = "Delete from ModuleList Where MID=?;";
            PreparedStatement preparedStmt = con.prepareStatement(SQL);
            preparedStmt.setInt(1, selectedid);
            // execute the preparedstatement
            preparedStmt.execute();
            JOptionPane.showMessageDialog(new JFrame(),"Module deleted successfully.");
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(new JFrame(),"Error while deleting module."+ex);
        }
        if(this.jComboBox1.getSelectedIndex()>=0 && this.jComboBox2.getSelectedIndex()>=0)
        LoadModule(this.jComboBox1.getSelectedItem().toString(),this.jComboBox2.getSelectedItem().toString());
    }//GEN-LAST:event_jButton2ActionPerformed

